      <DIV CLASS="textboxshrunken" ID="goldChannelMarch">
        <h3><font color="red">T<font color="green">I<font color="red">N<font color="green">S<font color="red">E<font color="green">L<font color=" yellow"> CHANNEL</font></h3><p><font color="black">
        Welcome to the last Genesis meeting in 2014, a year in which one might be forgiven for needing a snorkel &amp; flippers to get to work, &amp; the new Star Wars filming was the biggest thing to happen on Greenham Common since Ronald Ray-Gun &amp; friends set up thier own Star Wars re-enactment LARP there in the 1980's.  After the last instalment of the Hobbit hits our screens, Peter Jackson is also looking for a new purpose in life.  The club's pub venue got a facelift that revealed that YES, VIRGINIA, there IS a pattern on the carpet.<br><br>
        And despite severe temptation (and LONCON) the club is still solvent.<br><br>
        This afternoon's timetable is presented by the Committee, &amp; takes a walk on the lighter side of sci-fi.  WALL-E isn't a holiday film, &amp; neither is Gremlins, but both are a fun end to the year that don't involve shooting - or Santa Claus vs the Martians, which is the third option.  Doctor Who's Christmas specials are always enjoyable, particularly when they introduce a new companion or Doctor.  I always mishear the theme song to The Big Bang Theory as 'mad science', which sums up Sheldon Cooper quite nicely.<br><br>
        Enjoy the show &amp; have a peaceful holiday season.  See you at the meal on 28th December, which has now switched venue, &amp; will be at The Swan in Sherborne St John* (12 noon)<br><br>
        <br>
        The Chair<br>
